#include <stdio.h>

int main() {
    int    i    = 10;        
    float  f    = 3.14f;     
    double d    = 3.141592;  
    char   c    = 'R';       

    printf("integer: %d\n", i);      
    printf("float: %f\n", f);      
    printf("double: %lf\n", d);    
    printf("character: %c\n", c);      

    return 0;
}
